package com;

public class if_else {

	public static void main(String[] args) {
		int a = 3, b = 6, c = 4;
		if(a>=b) {
			System.out.println("a is greater");
		}
		else if(b>=c) {
			System.out.println("b is greater");
		}
		else if(c>=a) {
			System.out.println("c is greater");
		}

	}

}
